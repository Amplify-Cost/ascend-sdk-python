def test_immutable_audit_service():
    """Test 1: Verify ImmutableAuditService works correctly"""
    print(f"\n{BLUE}TEST 1: ImmutableAuditService Integration{RESET}")

    try:
        # Create in-memory database
        engine = create_engine("sqlite:///:memory:")
        Base.metadata.create_all(engine)
        Session = sessionmaker(bind=engine)
        db = Session()

        # Create audit service
        audit_service = ImmutableAuditService(db)

        # Test log_event method
        audit_log = audit_service.log_event(
            event_type="USER_ACTION",
            actor_id="test_user_123",
            resource_type="mcp_action",
            resource_id="action_456",
            action="CREATE",
            event_data={
                "action_type": "mcp_action_unified",
                "mcp_server": "claude-desktop",
                "namespace": "filesystem",
                "verb": "read_file",
                "resource": "/etc/passwd",
                "risk_level": "HIGH",
                "policy_decision": "DENY"
            },
            risk_level="HIGH",
            compliance_tags=["MCP_GOVERNANCE", "UNIFIED_ACTION"],
            ip_address="192.168.1.100",
            user_agent="Mozilla/5.0",
            session_id="session_789"
        )

        # Verify audit log created
        assert audit_log.id is not None
        assert audit_log.event_type == "USER_ACTION"
        assert audit_log.actor_id == "test_user_123"
        assert audit_log.resource_id == "action_456"
        assert audit_log.risk_level == "HIGH"
        assert "MCP_GOVERNANCE" in audit_log.compliance_tags

        # Verify hash chaining
        assert audit_log.content_hash is not None
        assert audit_log.chain_hash is not None

        print_test("ImmutableAuditService.log_event()", True,
                   f"Created audit log {audit_log.id} with hash chain")

        # Test chain integrity
        integrity_check = audit_service.verify_chain_integrity()
        assert integrity_check.status == "VALID"

        print_test("Chain integrity verification", True,
                   f"Status: {integrity_check.status}, Records: {integrity_check.total_records}")

        db.close()
        return True

    except Exception as e:
        print_test("ImmutableAuditService Integration", False, str(e))
        return False

def test_log_audit_trail_schema():
    """Test 2: Verify LogAuditTrail schema uses only valid fields"""
    print(f"\n{BLUE}TEST 2: LogAuditTrail Schema Validation{RESET}")

    try:
        # Create in-memory database
        engine = create_engine("sqlite:///:memory:")
        Base.metadata.create_all(engine)
        Session = sessionmaker(bind=engine)
        db = Session()

        # Create user for foreign key
        user = User(
            email="test@example.com",
            password="hashed_password",
            role="admin"
        )
        db.add(user)
        db.commit()

        # Test CORRECT LogAuditTrail creation (without invalid fields)
        audit_trail = LogAuditTrail(
            user_id=user.id,
            action="DELETE",  # NOT action_id
            resource_type="governance_policy",
            resource_id="policy_123",
            details={  # JSON, NOT string
                "policy_name": "Test Policy",
                "action_type": "delete_policy",
                "deleted_by": "admin@example.com"
            },
            risk_level="MEDIUM",
            compliance_framework="GOVERNANCE"
            # NOT timestamp - auto-generated by Base model
            # NOT user_email - not in schema
        )

        db.add(audit_trail)
        db.commit()

        # Verify creation
        assert audit_trail.id is not None
        assert audit_trail.user_id == user.id
        assert audit_trail.action == "DELETE"
        assert audit_trail.resource_type == "governance_policy"
        assert isinstance(audit_trail.details, dict)

        print_test("LogAuditTrail with VALID fields", True,
                   f"Created audit trail {audit_trail.id} without schema errors")

        # Test that INVALID fields are rejected
        try:
            invalid_trail = LogAuditTrail(
                user_id=user.id,
                action_id=123,  # INVALID - doesn't exist
                user_email="test@example.com",  # INVALID - doesn't exist
                timestamp=datetime.now(UTC)  # INVALID - auto-generated
            )
            db.add(invalid_trail)
            db.commit()
            print_test("LogAuditTrail rejects invalid fields", False,
                       "Should have rejected invalid fields")
            return False
        except TypeError as e:
            print_test("LogAuditTrail rejects invalid fields", True,
                       f"Correctly rejected: {str(e)[:50]}...")

        db.close()
        return True

    except Exception as e:
        print_test("LogAuditTrail Schema Validation", False, str(e))
        return False

