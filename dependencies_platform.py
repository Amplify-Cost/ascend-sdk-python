def require_platform_scope(user_context: dict) -> None:
    """
    Validate that user has platform scope.

    Use in routes to add additional scope checks.

    Raises:
        HTTPException 403: If user is not platform scope
    """
    if user_context.get("scope") != "platform":
        raise HTTPException(
            status_code=status.HTTP_403_FORBIDDEN,
            detail="Platform access required"
        )


def require_org_scope(user_context: dict) -> None:
    """
    Validate that user has organization scope.

    Use in routes to add additional scope checks.

    Raises:
        HTTPException 403: If user is not org scope
    """
    if user_context.get("scope") != "org":
        raise HTTPException(
            status_code=status.HTTP_403_FORBIDDEN,
            detail="Organization access required"
        )


def get_org_id_or_none(user_context: dict) -> Optional[int]:
    """
    Get organization_id from user context, or None for platform users.

    Returns:
        int: Organization ID for org users
        None: For platform users
    """
    return user_context.get("organization_id")


def require_org_id(user_context: dict) -> int:
    """
    Get organization_id, raising error if not present.

    Use in routes that require an organization context.

    Returns:
        int: Organization ID

    Raises:
        HTTPException 403: If no organization_id (platform user)
    """
    org_id = user_context.get("organization_id")
    if org_id is None:
        raise HTTPException(
            status_code=status.HTTP_403_FORBIDDEN,
            detail="This route requires organization context"
        )
    return org_id
