@router.get("/health", response_model=PlatformHealthStatus)
async def get_platform_health(
    db: Session = Depends(get_db),
    current_user: dict = Depends(get_current_platform_user)
):
    """
    Get platform-wide health status.

    Security:
    - Requires platform scope (OWKAI Internal Cognito pool)
    - Returns aggregate metrics only

    Returns:
        Platform health status with high-level metrics
    """
    require_platform_scope(current_user)

    try:
        # Test database connection
        db.execute(text("SELECT 1"))
        db_status = "connected"
    except Exception as e:
        logger.error(f"Database health check failed: {e}")
        db_status = "error"

    # Get organization counts
    total_orgs = db.query(Organization).count()
    active_orgs = db.query(Organization).filter(
        Organization.subscription_status.in_(['active', 'trial'])
    ).count()

    # Get user count
    total_users = db.query(User).count()

    # Determine overall status
    if db_status == "error":
        overall_status = "unhealthy"
    elif active_orgs == 0:
        overall_status = "degraded"
    else:
        overall_status = "healthy"

    logger.info(f"Platform health check by {current_user.get('email')}: {overall_status}")

    return PlatformHealthStatus(
        status=overall_status,
        timestamp=datetime.utcnow(),
        database=db_status,
        total_organizations=total_orgs,
        active_organizations=active_orgs,
        total_users=total_users,
        uptime_percentage=99.9  # TODO: Integrate with actual monitoring
    )


