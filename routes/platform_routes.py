@router.post("/orgs/{org_id}/suspend", response_model=ActionResponse)
async def suspend_organization(
    org_id: int,
    request: SuspendRequest,
    req: Request,
    db: Session = Depends(get_db),
    current_user: dict = Depends(get_current_platform_user)
):
    """
    Suspend an organization.

    Security:
    - Requires platform scope (OWKAI Internal Cognito pool)
    - Logs action for audit trail
    - Does not delete any data

    Effects:
    - Sets subscription_status to 'suspended'
    - Users cannot log in until reactivated
    - All data preserved for compliance

    Args:
        org_id: Organization ID to suspend
        request: Suspension reason

    Returns:
        Action confirmation
    """
    require_platform_scope(current_user)

    org = db.query(Organization).filter(Organization.id == org_id).first()
    if not org:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail="Organization not found"
        )

    if org.subscription_status == 'suspended':
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="Organization is already suspended"
        )

    # Store previous status for audit
    previous_status = org.subscription_status

    # Suspend the organization
    org.subscription_status = 'suspended'
    db.commit()

    # Log the action
    logger.warning(
        f"PLATFORM ACTION: Organization {org_id} ({org.name}) suspended by "
        f"{current_user.get('email')}. Reason: {request.reason}. "
        f"Previous status: {previous_status}"
    )

    # TODO: Add to audit log table when available
    # audit_log_entry = AuditLog(
    #     action="organization_suspended",
    #     performed_by=current_user.get('email'),
    #     organization_id=org_id,
    #     details={"reason": request.reason, "previous_status": previous_status}
    # )

    return ActionResponse(
        success=True,
        message=f"Organization '{org.name}' has been suspended",
        organization_id=org_id,
        action="suspend",
        performed_by=current_user.get('email'),
        timestamp=datetime.utcnow()
    )


@router.post("/orgs/{org_id}/activate", response_model=ActionResponse)
async def activate_organization(
    org_id: int,
    request: ActivateRequest,
    req: Request,
    db: Session = Depends(get_db),
    current_user: dict = Depends(get_current_platform_user)
):
    """
    Activate a suspended organization.

    Security:
    - Requires platform scope (OWKAI Internal Cognito pool)
    - Logs action for audit trail

    Effects:
    - Sets subscription_status to 'active'
    - Users can log in again

    Args:
        org_id: Organization ID to activate
        request: Optional activation note

    Returns:
        Action confirmation
    """
    require_platform_scope(current_user)

    org = db.query(Organization).filter(Organization.id == org_id).first()
    if not org:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail="Organization not found"
        )

    if org.subscription_status != 'suspended':
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail=f"Organization is not suspended (current status: {org.subscription_status})"
        )

    # Activate the organization
    org.subscription_status = 'active'
    db.commit()

    # Log the action
    logger.info(
        f"PLATFORM ACTION: Organization {org_id} ({org.name}) activated by "
        f"{current_user.get('email')}. Note: {request.reason or 'None provided'}"
    )

    return ActionResponse(
        success=True,
        message=f"Organization '{org.name}' has been activated",
        organization_id=org_id,
        action="activate",
        performed_by=current_user.get('email'),
        timestamp=datetime.utcnow()
    )
