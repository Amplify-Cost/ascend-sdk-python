@router.get("/orgs", response_model=List[OrganizationMetadata])
async def list_organizations(
    skip: int = 0,
    limit: int = 100,
    status_filter: Optional[str] = None,
    tier_filter: Optional[str] = None,
    db: Session = Depends(get_db),
    current_user: dict = Depends(get_current_platform_user)
):
    """
    List all organizations on the platform (metadata only).

    Security:
    - Requires platform scope (OWKAI Internal Cognito pool)
    - Returns metadata only (no customer business data)
    - Supports pagination and filtering

    Args:
        skip: Pagination offset
        limit: Maximum results (max 100)
        status_filter: Filter by subscription status
        tier_filter: Filter by subscription tier

    Returns:
        List of organization metadata
    """
    require_platform_scope(current_user)

    # Enforce max limit
    limit = min(limit, 100)

    # Build query
    query = db.query(Organization)

    if status_filter:
        query = query.filter(Organization.subscription_status == status_filter)
    if tier_filter:
        query = query.filter(Organization.subscription_tier == tier_filter)

    orgs = query.order_by(Organization.created_at.desc()).offset(skip).limit(limit).all()

    # Enrich with user counts
    results = []
    for org in orgs:
        user_count = db.query(User).filter(User.organization_id == org.id).count()

        # Check if suspended
        is_suspended = org.subscription_status == 'suspended'

        results.append(OrganizationMetadata(
            id=org.id,
            name=org.name,
            slug=org.slug,
            subscription_tier=org.subscription_tier,
            subscription_status=org.subscription_status,
            created_at=org.created_at,
            user_count=user_count,
            is_suspended=is_suspended
        ))

    logger.info(f"Platform user {current_user.get('email')} listed {len(results)} organizations")

    return results


@router.get("/orgs/{org_id}", response_model=OrganizationDetailMetadata)
async def get_organization(
    org_id: int,
    db: Session = Depends(get_db),
    current_user: dict = Depends(get_current_platform_user)
):
    """
    Get detailed organization metadata.

    Security:
    - Requires platform scope (OWKAI Internal Cognito pool)
    - Returns metadata only (no customer business data)

    Args:
        org_id: Organization ID

    Returns:
        Detailed organization metadata
    """
    require_platform_scope(current_user)

    org = db.query(Organization).filter(Organization.id == org_id).first()
    if not org:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail="Organization not found"
        )

    # Get user count
    user_count = db.query(User).filter(User.organization_id == org.id).count()

    # Get action counts
    thirty_days_ago = datetime.utcnow() - timedelta(days=30)
    seven_days_ago = datetime.utcnow() - timedelta(days=7)
    one_day_ago = datetime.utcnow() - timedelta(days=1)

    action_count_30d = db.query(AgentAction).filter(
        AgentAction.organization_id == org.id,
        AgentAction.created_at >= thirty_days_ago
    ).count()

    action_count_7d = db.query(AgentAction).filter(
        AgentAction.organization_id == org.id,
        AgentAction.created_at >= seven_days_ago
    ).count()

    action_count_24h = db.query(AgentAction).filter(
        AgentAction.organization_id == org.id,
        AgentAction.created_at >= one_day_ago
    ).count()

    # Get last activity
    last_action = db.query(AgentAction).filter(
        AgentAction.organization_id == org.id
    ).order_by(AgentAction.created_at.desc()).first()

    is_suspended = org.subscription_status == 'suspended'

    logger.info(f"Platform user {current_user.get('email')} viewed org {org_id}")

    return OrganizationDetailMetadata(
        id=org.id,
        name=org.name,
        slug=org.slug,
        subscription_tier=org.subscription_tier,
        subscription_status=org.subscription_status,
        created_at=org.created_at,
        user_count=user_count,
        is_suspended=is_suspended,
        action_count_30d=action_count_30d,
        action_count_7d=action_count_7d,
        action_count_24h=action_count_24h,
        last_activity=last_action.created_at if last_action else None,
        suspended_at=None,  # TODO: Add to Organization model if needed
        suspended_by=None,
        suspension_reason=None
    )


