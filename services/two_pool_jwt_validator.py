_validator: Optional[MultiPoolJWTValidator] = None
_validator_lock = Lock()


def get_validator() -> MultiPoolJWTValidator:
    """Get or create the singleton validator instance."""
    global _validator

    if _validator is None:
        with _validator_lock:
            if _validator is None:
                _validator = MultiPoolJWTValidator()

    return _validator


def validate_token(token: str, db: Session, allow_fallback: bool = True) -> dict:
    """
    Convenience function to validate a token.

    Args:
        token: JWT token string
        db: Database session for org lookup
        allow_fallback: If True, attempt fallback validation on unknown issuer

    Returns:
        Validated payload with scope, pool_source, and org_id
    """
    validator = get_validator()
    return validator.validate(token, db)
