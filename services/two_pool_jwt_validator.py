# JWKS CACHE (Per-Pool)
# =============================================================================

class JWKSCache:
    """
    Thread-safe JWKS cache with per-pool key storage.

    Features:
    - 60-minute TTL per pool
    - Automatic refresh on cache miss
    - Thread-safe operations
    """

    TTL_SECONDS = 3600  # 60 minutes

    def __init__(self):
        self._cache: Dict[str, Dict[str, RSAKey]] = {}
        self._timestamps: Dict[str, datetime] = {}
        self._lock = Lock()

    def get_keys(self, pool: PoolConfig) -> Dict[str, RSAKey]:
        """
        Get JWKS keys for a pool, fetching if needed.

        Args:
            pool: Pool configuration

        Returns:
            Dict mapping key ID (kid) to RSAKey
        """
        with self._lock:
            pool_id = pool.pool_id
            now = datetime.utcnow()

            # Check if cached and not expired
            if pool_id in self._cache:
                cached_at = self._timestamps.get(pool_id)
                if cached_at and (now - cached_at).total_seconds() < self.TTL_SECONDS:
                    return self._cache[pool_id]

            # Fetch fresh keys
            keys = self._fetch_keys(pool)
            self._cache[pool_id] = keys
            self._timestamps[pool_id] = now

            return keys

    def _fetch_keys(self, pool: PoolConfig) -> Dict[str, RSAKey]:
        """Fetch JWKS keys from Cognito endpoint."""
        try:
            logger.info(f"Fetching JWKS from {pool.jwks_url}")
            response = requests.get(pool.jwks_url, timeout=10)
            response.raise_for_status()

            keys_data = response.json()["keys"]
            keys = {}

            for key_data in keys_data:
                kid = key_data["kid"]
                keys[kid] = jwk.construct(key_data)

            logger.info(f"Fetched {len(keys)} keys for pool {pool.pool_id}")
            return keys

        except Exception as e:
            logger.error(f"Failed to fetch JWKS for pool {pool.pool_id}: {e}")
            raise

    def refresh(self, pool: PoolConfig) -> Dict[str, RSAKey]:
        """Force refresh keys for a pool."""
        with self._lock:
            if pool.pool_id in self._cache:
                del self._cache[pool.pool_id]
            if pool.pool_id in self._timestamps:
                del self._timestamps[pool.pool_id]

        return self.get_keys(pool)

    def clear(self):
        """Clear all cached keys."""
        with self._lock:
            self._cache.clear()
            self._timestamps.clear()


