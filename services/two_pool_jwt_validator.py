# ORGANIZATION POOL LOOKUP
# =============================================================================

class OrgPoolResolver:
    """
    Resolves organization pools from database.

    Caches org pool configs to avoid repeated DB queries.
    """

    CACHE_TTL_SECONDS = 300  # 5 minutes

    def __init__(self):
        self._cache: Dict[str, Tuple[PoolConfig, datetime]] = {}  # issuer -> (config, timestamp)
        self._lock = Lock()

    def resolve_by_issuer(self, issuer: str, db: Session) -> Optional[PoolConfig]:
        """
        Resolve organization pool config from issuer URL.

        Args:
            issuer: Cognito issuer URL (e.g., https://cognito-idp.us-east-2.amazonaws.com/us-east-2_xxx)
            db: Database session

        Returns:
            PoolConfig if found, None otherwise
        """
        # Check cache first
        with self._lock:
            if issuer in self._cache:
                config, cached_at = self._cache[issuer]
                if (datetime.utcnow() - cached_at).total_seconds() < self.CACHE_TTL_SECONDS:
                    return config

        # Extract pool ID from issuer URL
        # Format: https://cognito-idp.{region}.amazonaws.com/{pool_id}
        try:
            pool_id = issuer.split("/")[-1]
            region = issuer.split(".")[1].replace("cognito-idp.", "")
        except (IndexError, AttributeError):
            logger.error(f"Invalid issuer format: {issuer}")
            return None

        # Query database for organization with this pool
        try:
            from models import Organization

            org = db.query(Organization).filter(
                Organization.cognito_user_pool_id == pool_id,
                Organization.cognito_pool_status == "active"
            ).first()

            if not org:
                logger.warning(f"No active organization found for pool {pool_id}")
                return None

            config = PoolConfig(
                pool_id=pool_id,
                client_id=org.cognito_app_client_id,
                region=org.cognito_region or region,
                scope="org",
                organization_id=org.id
            )

            # Cache the result
            with self._lock:
                self._cache[issuer] = (config, datetime.utcnow())

            logger.info(f"Resolved pool {pool_id} to org {org.id} ({org.name})")
            return config

        except Exception as e:
            logger.error(f"Failed to resolve org pool: {e}")
            return None

    def clear_cache(self):
        """Clear the org pool cache."""
        with self._lock:
            self._cache.clear()


