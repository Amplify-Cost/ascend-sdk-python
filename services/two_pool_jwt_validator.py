# MULTI-POOL JWT VALIDATOR
# =============================================================================

class MultiPoolJWTValidator:
    """
    Validates JWTs from Platform and Per-Organization Cognito pools.

    Validation Flow:
    1. Extract issuer (iss) from unverified token
    2. Check if issuer matches platform pool → scope='platform'
    3. Otherwise, lookup organization by pool ID → scope='org'
    4. Fetch JWKS keys for that pool (cached)
    5. Verify RS256 signature
    6. Validate all claims (iss, aud, exp, token_use)
    7. Return validated payload with scope and org_id
    """

    def __init__(self):
        self.platform_pool = get_platform_pool_config()
        self.fallback_pool = get_fallback_customer_pool_config()
        self.jwks_cache = JWKSCache()
        self.org_resolver = OrgPoolResolver()

        logger.info(f"MultiPoolJWTValidator initialized")
        logger.info(f"  Platform pool: {self.platform_pool.pool_id if self.platform_pool else 'NOT CONFIGURED'}")
        logger.info(f"  Fallback pool: {self.fallback_pool.pool_id if self.fallback_pool else 'NOT CONFIGURED'}")

    def _extract_issuer(self, token: str) -> Optional[str]:
        """Extract issuer from token without verification."""
        try:
            claims = jwt.get_unverified_claims(token)
            return claims.get("iss")
        except JWTError:
            return None

    def identify_pool(self, token: str, db: Session) -> Optional[PoolConfig]:
        """
        Identify which pool issued a token.

        Priority:
        1. Platform pool (if issuer matches)
        2. Per-org pool (lookup by issuer in database)
        3. Fallback pool (backward compatibility)

        Args:
            token: JWT token string
            db: Database session for org lookup

        Returns:
            PoolConfig if pool identified, None otherwise
        """
        issuer = self._extract_issuer(token)
        if not issuer:
            logger.warning("Token missing 'iss' claim")
            return None

        # Check platform pool first
        if self.platform_pool and issuer == self.platform_pool.issuer:
            logger.debug(f"Token from platform pool")
            return self.platform_pool

        # Try to resolve from database
        org_pool = self.org_resolver.resolve_by_issuer(issuer, db)
        if org_pool:
            logger.debug(f"Token from org pool: org_id={org_pool.organization_id}")
            return org_pool

        # Fallback pool (backward compatibility)
        if self.fallback_pool and issuer == self.fallback_pool.issuer:
            logger.debug(f"Token from fallback pool")
            return self.fallback_pool

        logger.warning(f"Unknown issuer: {issuer}")
        return None

    def validate(self, token: str, db: Session) -> dict:
        """
        Validate a JWT token from any pool.

        Args:
            token: JWT token string
            db: Database session for org lookup

        Returns:
            Validated payload with added fields:
            - scope: 'platform' or 'org'
            - pool_source: Pool ID that issued the token
            - org_id: Organization ID (for org tokens)

        Raises:
            JWTError: If validation fails
        """
        # Step 1: Identify pool
        pool = self.identify_pool(token, db)
        if not pool:
            raise JWTError("Token issued by unknown pool - fail secure")

        # Step 2: Get unverified header for key ID
        header = jwt.get_unverified_header(token)
        kid = header.get("kid")

        if not kid:
            raise JWTError("Token missing 'kid' in header")

        # Step 3: Get public key from cache
        keys = self.jwks_cache.get_keys(pool)

        if kid not in keys:
            logger.info(f"Key {kid} not in cache, refreshing JWKS for {pool.pool_id}")
            keys = self.jwks_cache.refresh(pool)

            if kid not in keys:
                raise JWTError(f"Unknown key ID: {kid}")

        public_key = keys[kid]

        # Step 4: Verify signature and decode
        try:
            payload = jwt.decode(
                token,
                public_key,
                algorithms=["RS256"],
                audience=pool.client_id,
                issuer=pool.issuer,
                options={
                    "verify_aud": True,
                    "verify_iss": True,
                    "verify_exp": True,
                    "require_exp": True,
                }
            )
        except JWTError as e:
            logger.error(f"JWT verification failed for pool {pool.pool_id}: {e}")
            raise

        # Step 5: Validate token_use
        token_use = payload.get("token_use")
        if token_use not in ("id", "access"):
            raise JWTError(f"Invalid token_use: {token_use}")

        # Step 6: Enrich payload with scope info
        payload["scope"] = pool.scope
        payload["pool_source"] = pool.pool_id

        # Set org_id from pool config (for org pools resolved from DB)
        if pool.scope == "org" and pool.organization_id:
            payload["org_id"] = pool.organization_id
        elif pool.scope == "platform":
            payload["org_id"] = None

        logger.debug(f"Token validated: scope={pool.scope}, pool={pool.pool_id}, org_id={payload.get('org_id')}")

        return payload


