"""
RBAC-001: Multi-Pool JWT Validator (Platform + Per-Org Pools)
Generated by Ascend Engineering Team
Application Owner: OW-KAI Technologies, Inc.

Architecture:
  - Platform Pool (1): Ascend internal staff (scope='platform', org_id=null)
  - Tenant Pools (N): One per customer organization (scope='org', org_id=UUID)

Pool Resolution:
  1. Extract issuer (iss) from token
  2. Check if issuer matches platform pool → scope='platform'
  3. Otherwise, lookup organization by cognito_user_pool_id → scope='org'
  4. Fail secure if pool not found

Security Standards: SOC 2 CC6.1, NIST IA-5, PCI-DSS 8.2
"""

import os
import logging
import requests
from dataclasses import dataclass
from typing import Dict, Optional, Tuple
from datetime import datetime
from threading import Lock

from jose import jwt, jwk, JWTError
from jose.backends import RSAKey
from sqlalchemy.orm import Session

logger = logging.getLogger("enterprise.multi_pool_jwt")


# =============================================================================
# CONFIGURATION
# =============================================================================

@dataclass
class PoolConfig:
    """Configuration for a Cognito user pool."""
    pool_id: str
    client_id: str
    region: str
    scope: str  # 'platform' or 'org'
    organization_id: Optional[int] = None  # Set for org pools

    @property
    def issuer(self) -> str:
        """Cognito issuer URL."""
        return f"https://cognito-idp.{self.region}.amazonaws.com/{self.pool_id}"

    @property
    def jwks_url(self) -> str:
        """JWKS endpoint URL."""
        return f"{self.issuer}/.well-known/jwks.json"


def get_platform_pool_config() -> Optional[PoolConfig]:
    """
    Load platform pool configuration from environment variables.

    Returns:
        PoolConfig for platform pool, or None if not configured
    """
    region = os.getenv("COGNITO_REGION", "us-east-2")
    platform_pool_id = os.getenv("COGNITO_PLATFORM_POOL_ID")
    platform_client_id = os.getenv("COGNITO_PLATFORM_CLIENT_ID")

    if platform_pool_id and platform_client_id:
        logger.info(f"Platform pool configured: {platform_pool_id}")
        return PoolConfig(
            pool_id=platform_pool_id,
            client_id=platform_client_id,
            region=region,
            scope="platform",
            organization_id=None
        )

    logger.warning("Platform pool not configured (COGNITO_PLATFORM_POOL_ID missing)")
    return None


def get_fallback_customer_pool_config() -> Optional[PoolConfig]:
    """
    Get fallback customer pool from ENV (backward compatibility).
    Used when org lookup fails or for legacy tokens.
    """
    region = os.getenv("COGNITO_REGION", "us-east-2")
    pool_id = os.getenv("COGNITO_USER_POOL_ID")
    client_id = os.getenv("COGNITO_APP_CLIENT_ID")

    if pool_id and client_id:
        return PoolConfig(
            pool_id=pool_id,
            client_id=client_id,
            region=region,
            scope="org",
            organization_id=None  # Will be set from token claims
        )
    return None


